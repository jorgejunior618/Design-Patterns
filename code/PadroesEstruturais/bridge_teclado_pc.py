from abc import ABC, abstractmethod

class Computador(ABC):
  @abstractmethod
  def escreverCaractere(self, caractere: str):
    pass
  @abstractmethod
  def copiarTexto(self, txt: str):
    pass
  @abstractmethod
  def colarTexto(self, txt: str):
    pass
  @abstractmethod
  def alterarVolume(self, volume: int):
    pass
  @abstractmethod
  def mudo(self):
    pass
  @abstractmethod
  def obterVolume(self,):
    pass
  @abstractmethod
  def alterarReproducao(self, reproducao: str):
    pass

