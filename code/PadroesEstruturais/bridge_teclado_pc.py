class Teclado:
  def __init__(self, computador: Computador):
    self.computador = computador
  
  def digitar(self, caractere: str):
    return self.computador.escreverCaractere(caractere)
  
  def comando(self, caractere, valor=""):
    if (caractere == "c"):
      return self.computador.copiarTexto(valor)
    if (caractere == "v"):
      return self.computador.colarTexto()

